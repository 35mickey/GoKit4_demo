/* 串口接收空闲中断 */
void UsartReceive_IDLE(UART_HandleTypeDef *huart)  
{
	uint16_t i = 0;
	uint8_t check_xor = 0;
	
    if((__HAL_UART_GET_FLAG(huart,UART_FLAG_IDLE) != RESET))  
    {
		if(huart->Instance == USART1)
		{
			__HAL_UART_CLEAR_IDLEFLAG(huart);
			i = huart->Instance->SR;
			i = huart->Instance->DR;
			i = hdma_usart1_rx.Instance->CNDTR; 
			HAL_UART_DMAStop(huart);
			
			//router_parse(USART1_RX_MAX_SIZE - i);
			/* 解析串口数据 */
	
			if((USART1_RX_MAX_SIZE - i) == 7 && usart1_rx_buffer[0] == 0x55)	/* 1 HEAD，5 DATA, 1 XOR */
			{
				check_xor = 0;
				for(i = 0;i < 6;i++)
				{
					check_xor ^= usart1_rx_buffer[i];
				}
				if(check_xor == usart1_rx_buffer[6])
				{
					/* TODO: 常用变量更新 */
//					speed_value = usart1_rx_buffer[1];
					/* 暂时无视后退指令 */
					speed_value = ((signed char)usart1_rx_buffer[1])>=0?usart1_rx_buffer[1]:(-1*((signed char)usart1_rx_buffer[1]));
					dir_value = usart1_rx_buffer[2];
					pwm_en = usart1_rx_buffer[3];
					usart1_rx_flag = 1;
				}
			}
			
			/* 清空缓存，重新接收 */
			memset(usart1_rx_buffer,0x00,USART1_RX_MAX_SIZE);
			HAL_UART_Receive_DMA(huart,(uint8_t *)&usart1_rx_buffer,USART1_RX_MAX_SIZE);
		}
    }
}  

void usart1_send(unsigned char *buffer,unsigned int length)
{
	//等待上一次的数据发送完毕
	while(HAL_DMA_GetState(&hdma_usart1_tx) == HAL_DMA_STATE_BUSY)	osDelay(1);
	
	/* 关闭DMA */
	__HAL_DMA_DISABLE(&hdma_usart1_tx);
	
	//开始发送数据
	HAL_UART_Transmit_DMA(&huart1,buffer,length);
}

void usart1_printf(const char *format ,...)
{
	va_list arg;
	static char tx_buffer[256]={""};
	
	//把数据处理后放进缓冲区
	va_start(arg, format);
	vsprintf((char *)tx_buffer, format, arg);
	va_end(arg);
	
	//开始发送数据
	usart1_send((uint8_t *)tx_buffer,strlen(tx_buffer));
}

